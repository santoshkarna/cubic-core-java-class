package day4.basic;

public class VariableDemo {
    public static void main(String[] args) {
        //single declaration
        int a;
        //single initialization
        a = 5;

        //single declaration and initialization
        int b = 6;

        //multiple declaration
        int i, j, k;
        //multiple initialization
        i = 5;
        j = 6;
        k = 7;

        //multiple declaration and initialization
        int p = 5, q = 6, r = 7;

        //multiple declaration
        int x, y, z;
        //initializing with same value for all
        x = y = z = 5;

        int s = 5;
        int t = s;

    }
}
