package day4.basic;

public class DisplayDemo {

    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int sum = a + b;
        System.out.println(sum);
        System.out.println("sum is: " + sum);
        System.out.println("sum of a and b is: " + sum);
        System.out.println("sum of " + a + " and " + b + " is: " + sum);
        System.out.println("sum of a and b is: " + a + b);
        System.out.println("sum of a and b is: " + (a + b));
    }
}
